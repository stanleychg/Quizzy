/*
 * WordPair - Consists of a word its associated definition
 */

package wordgames.game.util;

public 	class WordPair {
	
	public String word;
	public String definition;
	
	public WordPair(){
		
	}
	
	public WordPair(String word, String definition){
		this.word = word;
		this.definition = definition;
	}
}
